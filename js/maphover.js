// button 01
$(function(){
        $(".btn-01").mouseover(function(){
            $(".mapbtn01").css("color","#f1d6a3");
            $("#guidemap").attr("src","images/map01.png");
            return false;
        });
        $(".btn-01").mouseout(function(){
            $(".mapbtn01").css("color","#fff");
            $("#guidemap").attr("src","images/map.png");
            return false;
        });
        $(".mapbtn01").mouseover(function(){
            $(".mapbtn01").css("color","#f1d6a3");
            $("#guidemap").attr("src","images/map01.png");
            return false;
        });
        $(".mapbtn01").mouseout(function(){
            $(".mapbtn01").css("color","#fff");
            $("#guidemap").attr("src","images/map.png");
            return false;
        });
        $(".btn-01").click(function(){
            $(".modal-01").css("display","block");        
            $(".nav_un").css("display","none");
        });
        $(".close-btn").click(function(){
            $(".modal-01").css("display","none");
            $(".nav_un").css("display","block");
        });
    });
// button 01 end

